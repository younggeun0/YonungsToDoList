package domain;

public class ToDo {

	private String td_num, content, finished_flag, input_date;

	public String getTd_num() {
		return td_num;
	}

	public void setTd_num(String td_num) {
		this.td_num = td_num;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getFinished_flag() {
		return finished_flag;
	}

	public void setFinished_flag(String finished_flag) {
		this.finished_flag = finished_flag;
	}

	public String getInput_date() {
		return input_date;
	}

	public void setInput_date(String input_date) {
		this.input_date = input_date;
	}
}
